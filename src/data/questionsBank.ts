export const getQuestionsBySubject = (subject: string): Question[] =>
  QUESTIONS_BANK.filter((q) => q.subject === subject);

export const getQuestionsByDifficulty = (difficulty: Difficulty): Question[] =>
  QUESTIONS_BANK.filter((q) => q.difficulty === difficulty);

export const getRandomQuestions = (count: number, subject?: string): Question[] => {
  const pool = subject ? getQuestionsBySubject(subject) : QUESTIONS_BANK;
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
};

